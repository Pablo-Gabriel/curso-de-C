/* programa para calculo de IMC */
#include<stdio.h>
#include<math.h>

const int limite = 30;

int main() {
    float peso, altura, IMC, potencia;

    printf("Qual seu peso e sua altura?\n");
    scanf(" %f %f",&peso, &altura);
    potencia = (altura * altura);
    IMC = peso / potencia;

    if( IMC <= limite) {
        printf("Você não está obeso!\n");
    } else {
        printf("Você está obeso!\n");
    };
    
    printf("Seu IMC é igual a: %.2f \n", IMC);

    return 0;
} 

